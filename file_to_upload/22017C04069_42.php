	<h1>Input Tag</h1> 
	<p>The <b>input type="password"</b> defines a password field:</p> 
	<form> 
    	<label for="uname">Username:</label><br> 
    	<input type="text" name="uname" id="uname"> 
    	<br><label for="pw">Password:</label><br> 
    	<input type="password" id="pw" name="pw" maxlength="12"> 
    	<br><br><input type="submit" name="submit"> 
	</form> 
	<p>The characters in a password field are masked (show as asterisks or circles).</p> 
